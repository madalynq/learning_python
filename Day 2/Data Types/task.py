#subscripting, telling the computer which character to print. we always start at 0, printing [0] will print "H"
print("Hello"[0])
print("Hello"[4])
# can also use [-1] or negative numbers, to count backwards. if you put [-1] it will print the last character of the string

#Integer = whole numbers
# use " " for strings, it will print what you type, for whole numbers to act like numbers, no need for " "
print(123 + 123)

#Large Integers, can use underscores instead of commas -- 1,000 --- 1_000, computer will ignore
print(1_000_000)

#float = floating point number
print(3.14159)

#boolean, True/False, always capitalized
print(True)
print(False)